/**
 * 자료형 검사
 * 자료형 확인 연산자 typeof
 */
console.log(typeof 10);
console.log(typeof "문자열");

//문자열
console.log(typeof("String"));
//숫자
console.log(typeof(273));
//불
console.log(typeof(true));
//함수
console.log(typeof(function () { }));
//객체
console.log(typeof({}));
//undefined : 변수를 선언했으나 초기화하지 않은 자료형
console.log(typeof(alpha));


