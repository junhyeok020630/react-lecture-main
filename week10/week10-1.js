/**
 * 리스트와 키
 * 리스트
 * 같은 아이템을 순서대로 모아 놓은 것
 * 배열 자료구조 형태
 * 변수나 객체를 하나의 변수로 묶어 놓음
 * 
 * 키
 * 각 객체나 아이템을 구분할 수 있는 고유한 값
 * 
 * 배열과 키를 사용하여 반복되는 다수의 엘리먼트 쉽게 렌더링
 * 
 * map() 함수 사용
 * 배열에 들어있는 각 변수에 어떤 처리를 한 뒤 리턴
 * 
 * 키
 * 리스트에서 아이템을 구분하기 위한 고유한 문자열
 * 리스트에서 어떤 아이템의 변경, 추가, 제거를 구분하기 위해 사용
 * 
 * 같은 리스트에 있는 엘리먼트 사이에서만 고유한 값이면 된다.
 */